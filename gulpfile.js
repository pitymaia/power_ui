var gulp = require('gulp');
var browserSync = require('browser-sync').create();
var insertLines = require('gulp-insert-lines');

var doNotEditThisFile = '<!-- *** ATENTION!!! *** File generated using gulp - DO NOT EDIT THIS FILE - user the real file in "source/index.html" -->'

function reloadTask(done) {
	browserSync.reload();
	done();
}

gulp.task('insert-styles-bundle', function (done) {
	gulp.src('source/index.html')
	.pipe(insertLines({
		'before': /<\/head>$/,
		'lineBefore': '    <script src="//console.re/connector.js" data-channel="power-ui-tests" id="consolerescript"></script>'
	}))
	.pipe(insertLines({
		'before': /<\/head>$/,
		'lineBefore': '    <script src="debug/show_errors.js"></script>'
	}))
	.pipe(insertLines({
		'before': '<!-- Porwer UI demo -->$',
		'lineBefore': doNotEditThisFile
	}))
	.pipe(gulp.dest('.'));
});

gulp.task('build', function (done) {
	gulp.src('source/index.html')
	.pipe(insertLines({
		'before': '<!-- Porwer UI demo -->$',
		'lineBefore': doNotEditThisFile
	}))
	.pipe(gulp.dest('.'));
	reloadTask(done);
});

// Static server
gulp.task('browser-sync', function() {
    browserSync.init({
        server: {
            baseDir: "./",
            routes: {
                "/": "index.html",
            },
        },
        startPath: '/',
    });

    gulp.watch("scripts/*.js").on('change', browserSync.reload);
    gulp.watch("css/*.css").on('change', browserSync.reload);
    // Reload 'build' on html change
    gulp.watch("source/*.html", ['build']);
});

gulp.task('default', ['build', 'browser-sync']);
gulp.task('debug', ['insert-styles-bundle', 'browser-sync']);
